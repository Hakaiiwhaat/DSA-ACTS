    private int deleteMethod() {
        if (createArray.numesLen == 0) {
            JOptionPane.showMessageDialog(this, "Array is not initialized.");
            return createArray.numesLen;
        }
        try {
//            for (int i=0;i<createArray.numesLen;i++){
//                System.out.println(numbers[i]);
//            }
            // Arrays.sort(numbers);
            int valFind = Integer.parseInt(jTextField1.getText());
            int j = find(valFind);
            if (j == createArray.numesLen) { // canâ€™t find it
                JOptionPane.showMessageDialog(this, "Value '" + valFind + "' not found.");
                // bool = false;
            } else {
                for (int k = j; k < createArray.numbers.length - 1; k++){ // move bigger ones down
                    createArray.numbers[k] = createArray.numbers[k + 1];
                }
                createArray.numesLen--; // decrement size
                createArray.numbers[createArray.numbers.length - 1] = 0; //set the deleted value to zero to not duplicate the values when deleting
                JOptionPane.showMessageDialog(this, "Value '" + valFind + "' deleted");
                // bool = true;
                
                insertArray.newNums = new int[createArray.numesLen]; //to set the new length of the array
                for (int i=0;i<createArray.numbers.length-1 ;i++){    
                    insertArray.newNums[i] = createArray.numbers[i];
                }
                createArray.numbers = insertArray.newNums;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Enter a number!");
        }
        System.out.println(createArray.numesLen);
        return createArray.numesLen;
    }
