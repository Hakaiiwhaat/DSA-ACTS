    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String checkIns = jTextField2.getText();
        if(checkIns.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No value is inserted!");
        } else {
            int numsToIns = Integer.parseInt(jTextField2.getText());
            insertFrame.nums.add(0, numsToIns);

            StringBuilder sb = new StringBuilder();
            sb.append("[ ");
            for (int num : insertFrame.nums) {
                sb.append(num).append(", ");
            }
            queueLabel.setText(sb.toString() + " ]");

            JOptionPane.showMessageDialog(this, "Value " + numsToIns + " has been queued at the FRONT!");
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

