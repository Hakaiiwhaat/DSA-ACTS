    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
            if (act10.isEmpty()) {
                act10.addFirst(Integer.parseInt(jTextField2.getText()));
                StringBuilder sb = new StringBuilder();
//                sb.append("[ ");
                for (int num : act10) {
                    sb.append(num);
                }
                linkedLabel.setText(sb.toString());
            } else {
                int pos = Integer.parseInt(jTextField3.getText());
                if (pos > act10.size()) {
                    JOptionPane.showMessageDialog(this, "Invalid position!");
                } else {
                    act10.add(pos - 1, Integer.parseInt(jTextField2.getText()));
                    System.out.println("Size is " + act10.size());
                    JOptionPane.showMessageDialog(this, "Value " + jTextField2.getText() + " inserted at postion " + pos);
                    StringBuilder sb = new StringBuilder();
//                    sb.append("[ ");
                    for (int i = 0; i < act10.size(); i++) {
                        if (i < act10.size() - 1) {
                            sb.append(act10.get(i)).append(" -> ");
                        } else {
                            sb.append(act10.get(i));
                        }
                    }
                    linkedLabel.setText(sb.toString());
                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Numbers only!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

