    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        try {
            if (act10.isEmpty()) {
                JOptionPane.showMessageDialog(this, "List is EMPTY!");
            } else {
                int pos = Integer.parseInt(jTextField3.getText());
                if (pos > act10.size()) {
                    JOptionPane.showMessageDialog(this, "Invalid position!");
                } else {
                    JOptionPane.showMessageDialog(this, "Value " + act10.get(Integer.parseInt(jTextField3.getText()) - 1) + " removed!");
                    act10.remove(Integer.parseInt(jTextField3.getText()) - 1);
                    StringBuilder sb = new StringBuilder();
//                    sb.append("[ ");
                    for (int i = 0; i < act10.size(); i++) {
                        if (i < act10.size() - 1) {
                            sb.append(act10.get(i)).append(" -> ");
                        } else {
                            sb.append(act10.get(i));
                        }
                    }
                    linkedLabel.setText(sb.toString());
                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Numbers only!");
        }
    }//GEN-LAST:event_jButton7ActionPerformed

