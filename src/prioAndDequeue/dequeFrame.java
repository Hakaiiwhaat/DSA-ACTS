    private void frontDeque() {
        try {
            int removeNums = insertFrame.nums.remove(0);
            JOptionPane.showMessageDialog(this, "Value " + removeNums + " has been removed!");
            jTextField1.setText(String.valueOf(insertFrame.nums));
            System.out.println(insertFrame.nums.size());
        } catch (IndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(this, "Array List is empty!");
        }
    }
    
    private void rearDeque() {
        try {
            int removeNums = insertFrame.nums.remove(insertFrame.nums.size() - 1);
            JOptionPane.showMessageDialog(this, "Value " + removeNums + " has been removed!");
            jTextField1.setText(String.valueOf(insertFrame.nums));
            System.out.println(insertFrame.nums.size());
        } catch (IndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(this, "Array List is empty!");
        }
    }
    
